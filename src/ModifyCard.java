    public static void deletecards(Card c) throws IOException {
        try {
            ArrayList<Card> oldlist = new ArrayList<Card>();
            ArrayList<Card> newlist = new ArrayList<Card>();
            Crypto cr = new Crypto();
            //dhmiourgeite mia lista me tis kartes tou xrhsth
            oldlist = cr.aesdecryptcards();
              //vazei se mia kainourgia lista kathe antikeimeno kartas to opoio einai diaforetiko tou c(karta diagrafhs)
            for (int i = 0; i < oldlist.size(); i++) {
                if (oldlist.get(i).toString().equals(c.toString()) == false) {
                    newlist.add(oldlist.get(i));
                }
            }
            //diagrefete to arxeio kartwn
            File f = new File(LoginUI.u.getUsername() + "/Cards");
            f.delete();
             //ola ta antikeimena kartwn ths listas newlist eisagwnte me aes encryption sto arxeio
            for (int i = 0; i < newlist.size(); i++) {
                cr.aesencryptcard(newlist.get(i));
            }

        } catch (NoSuchAlgorithmException | NoSuchPaddingException | InvalidKeyException | UnsupportedEncodingException | IllegalBlockSizeException | BadPaddingException | InvalidAlgorithmParameterException ex) {
            Logger.getLogger(ModifyCard.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

