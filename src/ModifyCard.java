    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            //otan o xrhsths epilexei search tote 
            ArrayList<Card> cardlist = new ArrayList<Card>();
            String searchtype = jTextField1.getText();
            String num = jTextField2.getText();
            Crypto cr = new Crypto();
            //dhmiourgeitai mia lista me oles ths kartes tou xrhsth
            cardlist = cr.aesdecryptcards();
            if (cardlist.isEmpty()) {
                JOptionPane.showMessageDialog(null, "You have no cards registered");
            } else {
                for (int i = 0; i < cardlist.size(); i++) {
                    //psaxnei oles tis kartes kai an vrei karta idiou typou kai arithmou me afta p edwse,kathws kai idiou onomatos xrhsth
                    if (cardlist.get(i).getType().equals(searchtype) && cardlist.get(i).getNumber().equals(num) && cardlist.get(i).getUsername().equals(LoginUI.u.getUsername())) {
                        c = cardlist.get(i); //to c ginetai h karta p vrethike
                        jTextField6.setText(c.getType()); //se kathe pedio mpainei to antistoixo stoixeio ths kartas
                        jTextField3.setText(c.getNumber());
                        jTextField4.setText(c.getExpdate());
                        jTextField5.setText(c.getVerifnum());
                    }
                }
            }

        } catch (NoSuchAlgorithmException | NoSuchPaddingException | InvalidKeyException | InvalidAlgorithmParameterException | IOException | IllegalBlockSizeException | BadPaddingException ex) {
            Logger.getLogger(ModifyCard.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

